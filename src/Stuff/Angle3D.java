package Stuff;

/*
Класс угол изменения положения
 */
public class Angle3D {
    public double AngleX;
    public double AngleY;
    public double AngleZ;

    public Angle3D(double AngleX, double AngleY, double AngleZ) {
        this.AngleX = AngleX;
        this.AngleY = AngleY;
        this.AngleZ = AngleZ;
    }
}
